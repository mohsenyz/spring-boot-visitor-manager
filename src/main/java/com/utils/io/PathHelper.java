package com.utils.io;

import java.io.File;

/**
 *
 * @author mphj
 */
public class PathHelper {

    public static String combine(String path1, String path2) {
        File file1 = new File(path1);
        File file2 = new File(file1, path2);
        return file2.getPath();
    }
}
